    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      String user,pwp;
        user=txtusuario.getText();
        pwp= txtpasw.getText();
      int areas=Comb.getSelectedIndex();
      
        if (areas==0){
            if(user.equals("ADMIN")&& pwp.equals("ADMINACC")){
            Administrador1 acceso=new Administrador1();
        acceso.setVisible(true);
        this.setVisible(false);
        
        }else{
        JOptionPane.showMessageDialog(null,"ACCESO ADMINISTRADOR DENEGADO ");
        }
            }
        if(areas==1){
            if(user.equals("Estudiante")&& pwp.equals("accestudiante")){
            Estudiante acceso=new Estudiante();
        acceso.setVisible(true);
        this.setVisible(false);
        
        }else{
        JOptionPane.showMessageDialog(null,"ACCESO ESTUDIANTE DENEGADO ");
        }
            }
        if(areas==2){
            if(user.equals("DOCENTE")&& pwp.equals("accdocente1")){
            Docente acceso=new Docente();
        acceso.setVisible(true);
        this.setVisible(false);
        
        }else{
        JOptionPane.showMessageDialog(null,"ACCESO MAESTRO DENEGADO ");
        }
            }
        
    }//GEN-LAST:event_jButton1ActionPerformed

